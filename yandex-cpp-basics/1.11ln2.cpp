#include <iostream>

#ifdef ACTIVE_MAIN
int main() {
    int n;
    std::cin >> n;

    double sum = 0.0;

    for (int k = 1; k <= n; k++) {
        // Вычисляем знак: (-1)^(k + 1)
        // k % 2 == 1 (нечетное): (-1)^(четное) = 1
        // k % 2 == 0 (четное): (-1)^(нечетное) = -1
        int sign;
        if (k % 2 == 1)
            sign = 1;
        else
            sign = -1;

        sum += sign / static_cast<double>(k);
    }

    std::cout << sum << std::endl;

    return 0;
}
#endif

