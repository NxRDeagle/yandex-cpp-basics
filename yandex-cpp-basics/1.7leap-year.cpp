#include <iostream>

#ifdef ACTIVE_MAIN
int main() {
    int year;
    std::cin >> year;

    // Проверяем условия високосного года по григорианскому календарю:
    // 1. Если год кратен 400 -> високосный
    // 2. Иначе, если год кратен 100 -> невисокосный
    // 3. Иначе, если год кратен 4 -> високосный
    // 4. Иначе -> невисокосный
    if (year % 400 == 0)
        std::cout << "YES" << std::endl;
    else if (year % 100 == 0)
        std::cout << "NO" << std::endl;
    else if (year % 4 == 0)
        std::cout << "YES" << std::endl;
    else
        std::cout << "NO" << std::endl;

    return 0;
}
#endif

/*
    Определите, является ли год високосным по григорианскому календарю.
    Вводится целое положительное четырёхзначное число — номер года.
    Программа выводит YES если год високосный и NO в противном случае.
*/