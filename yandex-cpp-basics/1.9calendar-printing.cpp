#include <iostream>
#include <iomanip>

#ifdef ACTIVE_MAIN
int main() {
    int n, k;
    std::cin >> n >> k;

    // Печатаем пробелы перед первым числом
    for (int i = 1; i < n; i++)
        std::cout << "   ";

    // Печатаем все числа месяца
    for (int day = 1; day <= k; day++) {
        // Выводим число с шириной 2
        std::cout << std::setw(2) << day;

        // Определяем, какой это день недели
        // (n + day - 1) % 7 дает 0 для воскресенья, 1-6 для других дней
        int day_of_week = (n + day - 1) % 7;

        // Если это суббота (6) или последний день месяца, перенос строки
        // Воскресенье - это 0
        if (day_of_week == 0 || day == k)
            std::cout << std::endl;
        else
            std::cout << " ";
    }

    return 0;
}
#endif

/*
    Напечатайте месяц из календаря по заданному начальному дню и количеству дней.
    Вводится два числа: n — номер дня недели первого числа месяца (целое число от 1 до 7) и k — количество дней в этом месяце (целое число от 1 до 99).
    n ≤ k. Обратите внимание, что число дней в месяце не обязательно должно быть таким же, как в привычном календаре.
    Пустые позиции в первой строке заполняйте пробелами.
    Соседние числа также разделяйте пробелами. Под одно число всегда выделяется два символа.
    В конце строчек до перевода строки пробелов быть не должно.
    Вывод должен завершаться ровно одним переводом строки.
*/