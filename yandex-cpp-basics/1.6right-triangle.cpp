#include <iostream>
#include <algorithm>


#ifdef ACTIVE_MAIN
int main() {
    long long a, b, c;
    std::cin >> a >> b >> c;

    // Проверяем условие существования треугольника:
    // сумма любых двух сторон должна быть больше третьей
    if (a + b <= c || a + c <= b || b + c <= a) {
        std::cout << "UNDEFINED" << std::endl;
        return 0;
    }

    // Для проверки на прямоугольность упорядочим стороны
    // и проверим теорему Пифагора: a² + b² = c²
    long long sides[3] = { a, b, c };

    // Сортируем стороны по возрастанию
    std::sort(sides, sides + 3);

    // Проверяем теорему Пифагора
    // Используем long long для избежания переполнения
    long long a_sq = sides[0] * sides[0];
    long long b_sq = sides[1] * sides[1];
    long long c_sq = sides[2] * sides[2];

    if (a_sq + b_sq == c_sq)
        std::cout << "YES" << std::endl;
    else
        std::cout << "NO" << std::endl;
    
    return 0;
}
#endif

